// 获取所有音频分类
export function getAudioCategories(): AudioCategory[] {
  return audioCategories;
}

// 获取特定分类的音频
export function getAudioByCategory(categoryId: string): AudioItem[] {
  const category = audioCategories.find(cat => cat.id === categoryId);
  return category ? category.items : [];
}

// 获取所有音频项目
export function getAllAudioItems(): AudioItem[] {
  return audioCategories.flatMap(category => category.items);
}

// 搜索音频
export function searchAudio(query: string): AudioItem[] {
  const lowerQuery = query.toLowerCase();
  return getAllAudioItems().filter(item => 
    item.title.toLowerCase().includes(lowerQuery) ||
    item.description.toLowerCase().includes(lowerQuery) ||
    item.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
  );
}

// 根据ID获取音频
export function getAudioById(id: string): AudioItem | undefined {
  return getAllAudioItems().find(item => item.id === id);
}

// 获取随机音频
export function getRandomAudio(): AudioItem | undefined {
  const items = getAllAudioItems();
  if (items.length === 0) return undefined;
  const randomIndex = Math.floor(Math.random() * items.length);
  return items[randomIndex];
}

// 根据标签过滤音频
export function getAudioByTag(tag: string): AudioItem[] {
  return getAllAudioItems().filter(item =>
    item.tags.some(t => t.toLowerCase().includes(tag.toLowerCase()))
  );
}

// 获取统计信息
export function getStats() {
  const items = getAllAudioItems();
  const allTags = items.flatMap(item => item.tags);
  const uniqueTags = Array.from(new Set(allTags));
  
  return {
    totalItems: items.length,
    totalTags: uniqueTags.length,
    categories: audioCategories.length,
    popularTags: uniqueTags.slice(0, 10)
  };
}
